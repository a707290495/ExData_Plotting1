Sys.setlocale("LC_TIME", "English")
library(chron)
d<-read.table("household_power_consumption.txt",sep=";",header = T,na.strings = "?")
d$Time<-times(d$Time)
d$Date<-as.Date(d$Date,'%d/%m/%Y')
d$Global_active_power<-as.numeric(d$Global_active_power)
m<-subset(d,(d$Date=="2007-02-01")|(d$Date=="2007-02-02"))
datetime<-strptime(paste(m$Date, m$Time, sep=" "), "%Y-%m-%d %H:%M:%S")
png("plot4.png",width=480,height=480)
par(mfrow=c(2,2))
plot(datetime,m$Global_active_power,type="l",ylab = "Global Active Power (kilowatts)",xlab="")
plot(datetime,m$Voltage,type="l",xlab="datetime",ylab="Voltage")
plot(datetime,m$Sub_metering_1,type="l",ylab = "Energy sub metering",xlab="",col="black")
lines(datetime,m$Sub_metering_2,type="l",xlab="",col="red")
lines(datetime,m$Sub_metering_3,type="l",xlab="",col="blue")
plot(datetime,m$Global_reactive_power,type="l")
legend("topright",lty =1,col=c("black","red","blue"),legend=c("Sub_metering_1","Sub_metering_2","Sub_metering_3"))
dev.off()